const state = {
  language: "en",
  site: null
};

const citizenForm = document.getElementById("citizenForm");
const passportForm = document.getElementById("passportForm");
const languageSwitcherEl = document.getElementById("languageSwitcher");
const ministryGridEl = document.getElementById("ministryGrid");
const territoryGridEl = document.getElementById("territoryGrid");
const newsGridEl = document.getElementById("newsGrid");
const alliesGridEl = document.getElementById("alliesGrid");

function t(key) {
  return I18N[state.language]?.[key] || I18N.en[key] || key;
}

function setStatus(id, text, type = "") {
  const el = document.getElementById(id);
  if (!el) return;
  el.textContent = text;
  el.classList.remove("is-success", "is-error");
  if (type) {
    el.classList.add(type === "success" ? "is-success" : "is-error");
  }
}

async function apiFetch(url, options = {}) {
  const headers = { "Content-Type": "application/json", ...(options.headers || {}) };
  const response = await fetch(url, { ...options, headers });
  const data = await response.json().catch(() => ({}));
  if (!response.ok) {
    throw new Error(data.error || "Request failed");
  }
  return data;
}

function renderLanguageSwitcher() {
  languageSwitcherEl.innerHTML = Object.keys(I18N).map((lang) => `
    <button type="button" data-lang="${lang}" class="${state.language === lang ? "is-active" : ""}">
      ${I18N[lang].languageName}
    </button>
  `).join("");
  languageSwitcherEl.querySelectorAll("[data-lang]").forEach((button) => {
    button.addEventListener("click", () => {
      state.language = button.dataset.lang;
      renderAll();
    });
  });
}

function renderTranslations() {
  document.documentElement.lang = state.language;
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-option]").forEach((el) => {
    el.textContent = t(el.dataset.i18nOption);
  });
}

function renderSiteFacts() {
  if (!state.site) return;
  document.title = state.site.name;
  document.getElementById("siteNameTop").textContent = state.site.name;
  document.getElementById("siteNameHero").textContent = state.site.name;
  document.getElementById("siteMotto").textContent = state.site.motto;
  document.getElementById("siteFounded").textContent = state.site.founded;
  document.getElementById("siteCapital").textContent = state.site.capital;
  document.getElementById("territoryCount").textContent = `${state.site.territories.length}`;
  document.getElementById("flagImage").src = state.site.flag;
}

function renderTerritoryOptions() {
  if (!state.site) return;
  const options = state.site.territories.map((territory) => `<option value="${territory.name}">${territory.name}</option>`).join("");
  document.getElementById("citizenTerritory").innerHTML = options;
  document.getElementById("passportTerritory").innerHTML = options;
}

function renderMinistries() {
  if (!state.site) return;
  ministryGridEl.innerHTML = state.site.ministries.map((ministry) => `
    <article class="ministry-card">
      <strong>${ministry.title || ministry.name}</strong>
      <div class="ministry-card__meta">${ministry.minister || t("ministryLead")}</div>
      <p>${ministry.description || ""}</p>
      <p class="record-card__meta">${ministry.focus || ""}</p>
    </article>
  `).join("");
}

function renderTerritories() {
  if (!state.site) return;
  territoryGridEl.innerHTML = state.site.territories.map((territory) => `
    <article class="card">
      <h3>${territory.name}</h3>
      <p>${territory.summary?.[state.language] || territory.summary?.en || ""}</p>
    </article>
  `).join("");
}

function renderNews() {
  if (!state.site) return;
  newsGridEl.innerHTML = state.site.news.map((item) => `
    <article class="card">
      <h3>${item.title}</h3>
      <p class="record-card__meta">${t("newsDate")}: ${item.date}</p>
      <p>${item.summary}</p>
    </article>
  `).join("");
}

function renderAllies() {
  if (!state.site) return;
  alliesGridEl.innerHTML = state.site.allies.map((ally) => `
    <article class="card">
      <h3>${ally.name}</h3>
      <p class="record-card__meta">${t("allyStatus")}: ${ally.status}</p>
      <p>${ally.notes}</p>
    </article>
  `).join("");
}

function renderAll() {
  renderLanguageSwitcher();
  renderTranslations();
  renderSiteFacts();
  renderTerritoryOptions();
  renderMinistries();
  renderTerritories();
  renderNews();
  renderAllies();
}

async function loadPublicData() {
  const data = await apiFetch("/api/public", { method: "GET" });
  state.site = data;
  renderAll();
}

citizenForm.addEventListener("submit", async (event) => {
  event.preventDefault();
  const formData = new FormData(citizenForm);
  try {
    await apiFetch("/api/citizen-applications", {
      method: "POST",
      body: JSON.stringify(Object.fromEntries(formData.entries()))
    });
    citizenForm.reset();
    setStatus("citizenStatus", t("citizenSubmitted"), "success");
  } catch (error) {
    setStatus("citizenStatus", error.message, "error");
  }
});

passportForm.addEventListener("submit", async (event) => {
  event.preventDefault();
  const formData = new FormData(passportForm);
  try {
    await apiFetch("/api/passport-applications", {
      method: "POST",
      body: JSON.stringify(Object.fromEntries(formData.entries()))
    });
    passportForm.reset();
    setStatus("passportStatus", t("passportSubmitted"), "success");
  } catch (error) {
    setStatus("passportStatus", error.message, "error");
  }
});

loadPublicData().catch(() => {
  setStatus("passportStatus", t("loadError"), "error");
});
